'''
 Calcule e escreva a área de um retângulo. - Ex. N° 6
'''

print(' =====================')
print(' | ÁREA DO RETÂNGULO |')
print(' =====================')

base = float(input(' Base do retângulo (metros): '))
print(' - - - - - -')
alto = float(input(' Altura do retângulo (metros): '))
print(' = = = = = = = = = = =')

area = base * alto

print(f' A área do retângulo é de {area} m2')
print(' =====================')